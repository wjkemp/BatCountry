#include "enemy.h"


//-----------------------------------------------------------------------------
Enemy::Enemy(int health) :
    WorldObject(0, 0),
    _health(health),
    _state(sActive)
{

}


//-----------------------------------------------------------------------------
Enemy::~Enemy()
{

}


//-----------------------------------------------------------------------------
void Enemy::damage(int damage)
{
    _health -= damage;
    if (_health <= 0) {
        _state = sKilled;
    }
}
